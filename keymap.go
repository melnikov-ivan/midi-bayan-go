package main

// BitToNote задаёт MIDI-ноту (0–127) для каждого бита (0–7) с регистра сдвига.
// Нумерация нот: C4 = 60, C#4 = 61, D4 = 62, … (стандарт MIDI).
// Можно менять значения под раскладку баяна/аккордеона.
var BitToNote = [8]uint8{
	60, // бит 0: C4
	61, // бит 1: C#4
	62, // бит 2: D4
	63, // бит 3: D#4
	64, // бит 4: E4
	65, // бит 5: F4
	66, // бит 6: F#4
	67, // бит 7: G4
}
